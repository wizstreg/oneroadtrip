/**
 * Obtenir la langue courante
 */
window.ORT_getLang = function() {
  return (localStorage.getItem('lang') || document.documentElement.lang || 'fr').slice(0,2);
};

/**
 * Traduire une clé
 * @param {string} key - Clé de traduction
 * @param {object} params - Paramètres optionnels pour interpolation
 * @returns {string} Texte traduit
 */
window.t = function(key, params) {
  const lang = window.ORT_getLang();
  let text = window.ORT_I18N[key]?.[lang] || window.ORT_I18N[key]?.fr || key;
  
  // Interpolation simple: t('hello', {name: 'Marc'}) avec "Bonjour {name}"
  if (params && typeof params === 'object') {
    Object.keys(params).forEach(k => {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), params[k]);
    });
  }
  
  return text;
};

/**
 * Pluraliser nuit/nuits
 * @param {number} n - Nombre
 * @returns {string} "X nuit" ou "X nuits"
 */
window.tNights = function(n) {
  const lang = window.ORT_getLang();
  const word = n > 1 ? window.ORT_I18N.nights[lang] : window.ORT_I18N.night[lang];
  return `${n} ${word || (n > 1 ? 'nights' : 'night')}`;
};

/**
 * Pluraliser jour/jours
 * @param {number} n - Nombre
 * @returns {string} "X jour" ou "X jours"
 */
window.tDays = function(n) {
  const lang = window.ORT_getLang();
  const word = n > 1 ? window.ORT_I18N.days[lang] : window.ORT_I18N.day[lang];
  return `${n} ${word || (n > 1 ? 'days' : 'day')}`;
};

/**
 * Appliquer les traductions aux éléments avec data-i18n
 */
window.applyI18N = function() {
  const lang = window.ORT_getLang();
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (window.ORT_I18N[key]?.[lang]) {
      el.textContent = window.ORT_I18N[key][lang];
    }
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.dataset.i18nTitle;
    if (window.ORT_I18N[key]?.[lang]) {
      el.title = window.ORT_I18N[key][lang];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (window.ORT_I18N[key]?.[lang]) {
      el.placeholder = window.ORT_I18N[key][lang];
    }
  });
};

// Auto-apply on DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', window.applyI18N);
} else {
  window.applyI18N();
}

